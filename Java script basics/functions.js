
function add(num1,num2){
    return num1+num2
}

console.log(add(2,2));


function cube(num){
    return num**3
}
console.log(cube(3));


function maxTwo(num1,num2){
    return num1<num2?num2:num1
}

console.log(maxTwo(4,3));

function smartSub(num1,num2){

    return num1>num2?num1-num2:num2-num1
}

console.log(smartSub(10,5));